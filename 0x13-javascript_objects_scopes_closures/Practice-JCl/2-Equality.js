// There is === & == sympols
// === type and valu
// == just value

console.log(5 === 5); // Output: true
console.log(5 === "5"); // Output: false
console.log(true === 1); // Output: false

console.log(5 == 5); // Output: true
console.log(5 == "5"); // Output: true
console.log(true == 1); // Output: true

console.log(0 == ''); // Output: true
console.log(0 === ''); // Output: false
console.log(false == ''); // Output: true
